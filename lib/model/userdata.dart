class UserData {
  String id;
  String password;
  String name;
  String avatar;
  String email;
  String phone;
  String gender;
  String disability;
  String address;

  UserData({
    required this.id,
    required this.password,
    required this.name,
    required this.avatar,
    required this.email,
    required this.phone,
    required this.gender,
    required this.disability,
    required this.address,
  });
}
